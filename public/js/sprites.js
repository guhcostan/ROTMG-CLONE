const Sprites = (() => {
  const cache = {};
  function build(name, grid) {
    const rows = grid.trim().split('\n').map(r => r.trim());
    const h = rows.length, w = Math.max(...rows.map(r => r.length));
    const cv = document.createElement('canvas');
    cv.width = w; cv.height = h;
    const ctx = cv.getContext('2d');
    rows.forEach((row, y) => {
      for (let x = 0; x < row.length; x++) {
        const ch = row[x];
        if (ch === '.' || !PAL[ch]) continue;
        ctx.fillStyle = PAL[ch];
        ctx.fillRect(x, y, 1, 1);
      }
    });
    cache[name] = cv;
    return cv;
  }
  function get(name) {
    if (cache[name]) return cache[name];
    if (SPRITE_DEFS[name]) return build(name, SPRITE_DEFS[name]);
    return null;
  }
  // item id -> sprite name
  function forItem(id, items) {
    const it = items[id];
    if (!it) return null;
    if (it.slot === 'weapon') return get(it.type);
    if (it.slot === 'ability') return get(it.type);
    if (it.slot === 'armor') return get(it.type);
    if (it.slot === 'ring') return get('ring');
    if (it.type === 'consumable') {
      if (id === 'hppot') return get('potion_red');
      if (id === 'mppot') return get('potion_blue');
      if (id === 'pot_life' || id === 'pot_mana') return get('potion_life');
      if (id === 'pet_egg') return get('egg');
      return get('potion_stat');
    }
    return null;
  }
  return { get, forItem };
})();
